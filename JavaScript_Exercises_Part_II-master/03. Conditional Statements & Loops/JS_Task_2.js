// Write a program that takes input a number from user & state 
// whether the number is positive, negative or zero.

/* --------------------------------------------------------------------------------- */

let number = prompt("Enter the number:");
if (number>=1){
    console.log("Positive");
}
else if(number == 0){
    console.log("Zero");
}
else{
    console.log("Negative");
}
